class CTHD:
    def __init__(self, mahd, madv, soluong, gia):
        self.mahd = mahd
        self.madv = madv
        self.soluong = soluong
        self.gia = gia

    def get_mahd(self):
        return self.mahd
    
    def set_mahd(self, mahd):
        self.mahd = mahd
    
    def get_madv(self):
        return self.madv
    
    def set_madv(self, madv):
        self.madv = madv

    def get_soLuong(self):
        return self.soluong
    
    def set_soLuong(self, soluong):
        self.soluong = soluong 

    def get_gia(self):
        return self.gia
    
    def set_gia(self, gia):
        self.gia = gia 